# Извлечь даты из строки:
# 'Amit 34-3456 12-05-2007, XYZ 56-4532 11-11-2011, 34-44-2000, 12-14-2000, ABC 67-8945 12-01-2009'

import re


string = 'Amit 34-3456 12-05-2007, XYZ 56-4532 11-11-2011, 34-44-2000, 12-14-2000, ABC 67-8945 12-01-2009'
result = re.findall(r'[0-3]\d-[01]\d-\d{4}', string)
print(result)
